"""  Tehtävänanto

Kirjoita ohjelma, joka tulostaa numerot yhdestä kymmeneen käyttäen while-silmukkaa.
 """

# Alustetaan muuttuja
num = 1

# Käytetään while-silmukkaa, kunnes num on suurempi kuin 10
while num <= 10:
    print(num)
    num += 1  # Kasvatetaan num-muuttujaa yhdellä jokaisen silmukan jälkeen
