"""  Tehtävänanto

Kirjoita funktio laske_summa, joka ottaa parametrinä kaksi lukua ja tulostaa niiden summan. """


# Määritellään funktio, joka ottaa kaksi parametria
def laske_summa(luku1, luku2):
    summa = luku1 + luku2
    print(f"Lukujen {luku1} ja {luku2} summa on {summa}.")

# Kutsutaan funktiota ja annetaan kaksi lukua parametreiksi
laske_summa(5, 7)
